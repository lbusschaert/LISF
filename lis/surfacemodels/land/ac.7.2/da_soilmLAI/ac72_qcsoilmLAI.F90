! !ROUTINE: ac72_qcsoilmLAI
! \label{ac72_qcsoilmLAI}
!
! !REVISION HISTORY:
! 27Feb2005: Sujay Kumar; Initial Specification
! 25Jun2006: Sujay Kumar: Updated for the ESMF design
! 1 Aug 2016: Mahdi Navari; Modified for ac72 
! 18 Jun 2021: Michel Bechtold: SM and Biomass updating with S1 backscatter w/ WCM
!
! !INTERFACE:
subroutine ac72_qcsoilmLAI(n, LSM_State)

! !USES:
  use ESMF
  use LIS_coreMod
  use LIS_logMod
  use ac72_lsmMod

  implicit none
! !ARGUMENTS: 
  integer, intent(in)    :: n
  type(ESMF_State)       :: LSM_State
!
! !DESCRIPTION:
!
!  Returns the soilmoisture related state prognostic variables for
!  data assimilation
! 
!  The arguments are: 
!  \begin{description}
!  \item[n] index of the nest \newline
!  \item[LSM\_State] ESMF State container for LSM state variables \newline
!  \end{description}
!EOP
  type(ESMF_Field)       :: sm1Field
  type(ESMF_Field)       :: sm2Field
  type(ESMF_Field)       :: sm3Field
  integer                :: t
  integer                :: status
  real, pointer          :: soilm1(:)
  real, pointer          :: soilm2(:)
  real, pointer          :: soilm3(:)
  real                   :: smmax1,smmax2,smmax3
  real                   :: smmin1,smmin2,smmin3
  type(ESMF_Field)       :: AC72BIOMASSField
  real, pointer          :: AC72BIOMASS(:)
  real                   :: AC72BIOMASSmax
  real                   :: AC72BIOMASSmin
  integer                :: gid
  real                   :: AC72BIOMASStmp

  logical                :: update_flag(LIS_rc%ngrid(n))
  real                   :: perc_violation(LIS_rc%ngrid(n))
  real                   :: AC72BIOMASSmean(LIS_rc%ngrid(n))
  integer                :: nAC72BIOMASSmean(LIS_rc%ngrid(n))
  integer                :: N_ens
  real                   :: state_tmp(LIS_rc%nensem(n)),state_mean

  call ESMF_StateGet(LSM_State,"Soil Moisture Layer 1",sm1Field,rc=status)
  call LIS_verify(status,&
       "ESMF_StateGet for Soil Moisture Layer 1 failed in ac72_qcsoilmLAI")

  call ESMF_FieldGet(sm1Field,localDE=0,farrayPtr=soilm1,rc=status)
  call LIS_verify(status,&
       "ESMF_FieldGet for Soil Moisture Layer 1 failed in ac72_qcsoilmLAI")

  call ESMF_AttributeGet(sm1Field,"Max Value",smmax1,rc=status)
  call LIS_verify(status,&
       "ESMF_AttributeGet: Max Value failed in ac72_qcsoilmLAI")

  call ESMF_AttributeGet(sm1Field,"Min Value",smmin1,rc=status)
  call LIS_verify(status,&
       "ESMF_AttributeGet: Min Value failed in ac72_qcsoilmLAI")

  do t=1,LIS_rc%npatch(n,LIS_rc%lsm_index)
     if(soilm1(t).gt.smmax1) soilm1(t) = smmax1
     if(soilm1(t).lt.smmin1) soilm1(t) = smmin1
  enddo

  ! Layer 2
  call ESMF_StateGet(LSM_State,"Soil Moisture Layer 2",sm2Field,rc=status)
  call LIS_verify(status,&
       "ESMF_StateGet for Soil Moisture Layer 2 failed in ac72_qcsoilmLAI")
 
  call ESMF_FieldGet(sm2Field,localDE=0,farrayPtr=soilm2,rc=status)
  call LIS_verify(status,&
       "ESMF_FieldGet for Soil Moisture Layer 2 failed in ac72_qcsoilmLAI")

  call ESMF_AttributeGet(sm2Field,"Max Value",smmax2,rc=status)
  call LIS_verify(status,&
       "ESMF_AttributeGet: Max Value failed in ac72_qcsoilmLAI")

  call ESMF_AttributeGet(sm2Field,"Min Value",smmin2,rc=status)
  call LIS_verify(status,&
       "ESMF_AttributeGet: Min Value failed in ac72_qcsoilmLAI")

  do t=1,LIS_rc%npatch(n,LIS_rc%lsm_index)

     if(soilm2(t).gt.smmax2) soilm2(t) = smmax2
     if(soilm2(t).lt.smmin2) soilm2(t) = smmin2
  enddo

  ! Layer 3
  call ESMF_StateGet(LSM_State,"Soil Moisture Layer 3",sm3Field,rc=status)
  call LIS_verify(status,&
       "ESMF_StateGet for Soil Moisture Layer 3 failed in ac72_qcsoilmLAI")
 
  call ESMF_FieldGet(sm3Field,localDE=0,farrayPtr=soilm3,rc=status)
  call LIS_verify(status,&
       "ESMF_FieldGet for Soil Moisture Layer 3 failed in ac72_qcsoilmLAI")

  call ESMF_AttributeGet(sm3Field,"Max Value",smmax3,rc=status)
  call LIS_verify(status,&
       "ESMF_AttributeGet: Max Value failed in ac72_qcsoilmLAI")

  call ESMF_AttributeGet(sm3Field,"Min Value",smmin3,rc=status)
  call LIS_verify(status,&
       "ESMF_AttributeGet: Min Value failed in ac72_qcsoilmLAI")

  do t=1,LIS_rc%npatch(n,LIS_rc%lsm_index)

     if(soilm3(t).gt.smmax3) soilm3(t) = smmax3
     if(soilm3(t).lt.smmin3) soilm3(t) = smmin3
  enddo

  ! BIOMASS
  call ESMF_StateGet(LSM_State,"AC72 BIOMASS",AC72BIOMASSField,rc=status)
  call LIS_verify(status,&
           "ESMF_StateGet for AC72BIOMASS failed in ac72_qcsoilmLAI")
  call ESMF_FieldGet(AC72BIOMASSField,localDE=0,farrayPtr=AC72BIOMASS,rc=status)
  call LIS_verify(status,&
           "ESMF_FieldGet for AC72BIOMASS failed in ac72_qcsoilmLAI")

  call ESMF_AttributeGet(AC72BIOMASSField,"Max Value",AC72BIOMASSmax,rc=status)
  call LIS_verify(status,&
           "ESMF_AttributeGet for AC72BIOMASS Max Value failed in ac72_qcsoilmLAI")
  call ESMF_AttributeGet(AC72BIOMASSField,"Min Value",AC72BIOMASSmin,rc=status)
  call LIS_verify(status,&
           "ESMF_AttributeGet for AC72BIOMASS Min Value failed in ac72_qcsoilmLAI")



  update_flag    = .true.
  perc_violation = 0.0
  AC72BIOMASSmean       = 0.0
  nAC72BIOMASSmean      = 0

  do t=1,LIS_rc%npatch(n,LIS_rc%lsm_index)

     gid = LIS_domain(n)%gindex(&
          LIS_surface(n,LIS_rc%lsm_index)%tile(t)%col,&
          LIS_surface(n,LIS_rc%lsm_index)%tile(t)%row)

     AC72BIOMASStmp =  AC72BIOMASS(t)

     if(AC72BIOMASStmp.lt.AC72BIOMASSmin.or.AC72BIOMASStmp.gt.AC72BIOMASSmax) then
        update_flag(gid) = .false.
        perc_violation(gid) = perc_violation(gid) +1
     endif

  enddo

  do gid=1,LIS_rc%ngrid(n)
     perc_violation(gid) = perc_violation(gid)/LIS_rc%nensem(n)
  enddo

! For ensembles that are unphysical, compute the
! ensemble average after excluding them. This
! is done only if the majority of the ensemble
! members are good (>60%)

  do t=1,LIS_rc%npatch(n,LIS_rc%lsm_index)

     gid = LIS_domain(n)%gindex(&
          LIS_surface(n,LIS_rc%lsm_index)%tile(t)%col,&
          LIS_surface(n,LIS_rc%lsm_index)%tile(t)%row)
     if(.not.update_flag(gid)) then
        if(perc_violation(gid).lt.0.8) then
           if((AC72BIOMASS(t).gt.AC72BIOMASSmin).and.&
                (AC72BIOMASS(t).lt.AC72BIOMASSmax)) then 
              AC72BIOMASSmean(gid) = AC72BIOMASSmean(gid) + &
                   AC72BIOMASS(t) 
              nAC72BIOMASSmean(gid) = nAC72BIOMASSmean(gid) + 1
           endif
        endif
     endif
  enddo
  
  do gid=1,LIS_rc%ngrid(n)
     if(nAC72BIOMASSmean(gid).gt.0) then
        AC72BIOMASSmean(gid) = AC72BIOMASSmean(gid)/nAC72BIOMASSmean(gid)
     endif
  enddo


  do t=1,LIS_rc%npatch(n,LIS_rc%lsm_index)
     gid = LIS_domain(n)%gindex(&
          LIS_surface(n,LIS_rc%lsm_index)%tile(t)%col,&
          LIS_surface(n,LIS_rc%lsm_index)%tile(t)%row)

     AC72BIOMASStmp =  AC72BIOMASS(t)

! If the update is unphysical, simply set to the average of
! the good ensemble members. If all else fails, do not
! update.

     if(update_flag(gid)) then
        AC72BIOMASS(t) = AC72BIOMASStmp
     elseif(perc_violation(gid).lt.0.8) then
        if(AC72BIOMASStmp.lt.AC72BIOMASSmin.or.AC72BIOMASStmp.gt.AC72BIOMASSmax) then
           AC72BIOMASS(t) = AC72BIOMASSmean(gid)
        else
           AC72BIOMASS(t) = AC72BIOMASS(t) 
        endif
     endif
  enddo

#if 0 
  N_ens = LIS_rc%nensem(n)
  do t=1,N_ens
     state_tmp(t) = AC72BIOMASS(t)
  enddo
  state_mean =sum(state_tmp)/N_ens

  write(113,fmt='(i4.4,i2.2,i2.2,i2.2,i2.2,i2.2,21F8.3)') &
       LIS_rc%yr, LIS_rc%mo, LIS_rc%da, LIS_rc%hr, &
       LIS_rc%mn, LIS_rc%ss, &
       state_mean, &
       state_tmp
#endif
end subroutine ac72_qcsoilmLAI

